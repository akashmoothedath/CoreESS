package vo;

public class UserdetailsVO {

	String daysOffTaken;
	String daysOffRemaining;
	String endDate;
	String rollOver;
	String manager;
	String recentStartDate;
	String recentEndDate;
	String recentDuration;
	String recentApprovedBy;
	
	public String getDaysOffTaken() {
		return daysOffTaken;
	}
	public void setDaysOffTaken(String daysOffTaken) {
		this.daysOffTaken = daysOffTaken;
	}
	public String getDaysOffRemaining() {
		return daysOffRemaining;
	}
	public void setDaysOffRemaining(String daysOffRemaining) {
		this.daysOffRemaining = daysOffRemaining;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public String getRollOver() {
		return rollOver;
	}
	public void setRollOver(String rollOver) {
		this.rollOver = rollOver;
	}
	public String getManager() {
		return manager;
	}
	public void setManager(String manager) {
		this.manager = manager;
	}
	public String getRecentStartDate() {
		return recentStartDate;
	}
	public void setRecentStartDate(String recentStartDate) {
		this.recentStartDate = recentStartDate;
	}
	public String getRecentEndDate() {
		return recentEndDate;
	}
	public void setRecentEndDate(String recentEndDate) {
		this.recentEndDate = recentEndDate;
	}
	public String getRecentDuration() {
		return recentDuration;
	}
	public void setRecentDuration(String recentDuration) {
		this.recentDuration = recentDuration;
	}
	public String getRecentApprovedBy() {
		return recentApprovedBy;
	}
	public void setRecentApprovedBy(String recentApprovedBy) {
		this.recentApprovedBy = recentApprovedBy;
	}

	
}
